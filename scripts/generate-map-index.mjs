import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const geoDir = "public/geojson/china";

function normalizeLevel(level) {
  if (level === "district") return "county";
  if (level === "province" || level === "city" || level === "county") return level;
  return "city";
}

function featureToRegion(feature, parentCode, provinceName) {
  const props = feature.properties ?? {};
  const code = String(props.adcode ?? props.code ?? "");
  const name = props.name;
  const center = props.center;
  if (!/^\d{6}$/.test(code) || !name) return undefined;
  const level = normalizeLevel(props.level);
  const region = {
    code,
    name,
    level,
    parentCode,
    center,
  };
  if (provinceName && level !== "province") region.province = provinceName;
  if (level === "province") region.geoJsonUrl = `/geojson/china/${code}.json`;
  return region;
}

async function main() {
  const provinceGeo = JSON.parse(await readFile(path.join(geoDir, "provinces.json"), "utf8"));
  const existing = new Set(await readdir(geoDir));
  const provinceRegions = provinceGeo.features
    .map((feature) => featureToRegion(feature, "100000"))
    .filter(Boolean)
    .filter((region) => region.code !== "100000")
    .map((region) => {
      const withParent = { ...region, parentCode: "100000" };
      if (existing.has(`${region.code}.json`)) withParent.geoJsonUrl = `/geojson/china/${region.code}.json`;
      return withParent;
    });

  const childRegions = [];
  for (const province of provinceRegions) {
    const file = `${province.code}.json`;
    if (!existing.has(file)) continue;
    const geojson = JSON.parse(await readFile(path.join(geoDir, file), "utf8"));
    for (const feature of geojson.features ?? []) {
      const region = featureToRegion(feature, province.code, province.name);
      if (region) childRegions.push(region);
    }
  }

  const all = [
    { code: "100000", name: "全国", level: "country", geoJsonUrl: "/geojson/china/provinces.json", center: [104, 35] },
    ...provinceRegions,
    ...childRegions,
  ];

  const seen = new Set();
  const deduped = all.filter((region) => {
    if (seen.has(region.code)) return false;
    seen.add(region.code);
    return true;
  });

  const output = `import type { Region } from "../types/dialect";

// Generated by scripts/generate-map-index.mjs from public/geojson/china/*.json.
export const generatedRegions: Region[] = ${JSON.stringify(deduped, null, 2)};
`;

  await writeFile("src/data/generatedRegions.ts", output, "utf8");
  await writeFile(
    "src/data/generatedRegions.report.json",
    `${JSON.stringify({ generatedAt: new Date().toISOString(), regionCount: deduped.length }, null, 2)}\n`,
    "utf8",
  );
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
